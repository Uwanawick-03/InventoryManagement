    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        int index = tableCustomer.getSelectedRow();

        if (index >= 0) {
            TableModel model = tableCustomer.getModel();

            String id = model.getValueAt(index, 0).toString();
            custPk = Integer.parseInt(id);

            String custName = model.getValueAt(index, 1).toString();
            txtCustName.setText(custName);

            String custPhone = model.getValueAt(index, 2).toString();
            txtCustPhone.setText(custPhone);

            String custEmail = model.getValueAt(index, 3).toString();
            txtCustEmail.setText(custEmail);
            
            String branch = model.getValueAt(index, 4).toString();
            comBoxBranch.setSelectedItem(branch);

            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
        }
    }//GEN-LAST:event_tableCustomerMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String custName = txtCustName.getText();
        String custPhone = txtCustPhone.getText();
        String custEmail = txtCustEmail.getText();
        String branch = (String) comBoxBranch.getSelectedItem();
        

        if (validateFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = DBConnection.DBHelper.getConnection();
                PreparedStatement pstmt = con.prepareStatement("update customer set Cust_Name=?, Phone=?, Email=?, Branch_Name=? where Cust_Id=?");
                pstmt.setString(1, custName);
                pstmt.setString(2, custPhone);
                pstmt.setString(3, custEmail);
                pstmt.setString(4, branch);
                pstmt.setInt(5, custPk);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Customer Updated Successfully");
                setVisible(true);
                loadCustomerData();
                clearForm();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String custName = txtCustName.getText();
        String custPhone = txtCustPhone.getText();
        String custEmail = txtCustEmail.getText();
        String branch = (String) comBoxBranch.getSelectedItem();

        if (validateFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = DBConnection.DBHelper.getConnection();
                PreparedStatement pstmt = con.prepareStatement("insert into customer (Cust_Name, Phone, Email, Branch_Name ) VALUES (?, ?, ?,?)");
                pstmt.setString(1, custName);
                pstmt.setString(2, custPhone);
                pstmt.setString(3, custEmail);
                pstmt.setString(4, branch);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Customer Registered Successfully");
                setVisible(true);
                loadCustomerData();
                clearForm();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String custName = txtCustName.getText();
        String custPhone = txtCustPhone.getText();
        String custEmail = txtCustEmail.getText();
        String branch = (String) comBoxBranch.getSelectedItem();
        
        if (validateFields("edit")) {
            JOptionPane.showMessageDialog(null, "Select Item To Delete");
        } else {
            int conDelete = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this customer?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
            if (conDelete == 0) {
                try {
                    Connection con = DBConnection.DBHelper.getConnection();
                    PreparedStatement pstmt = con.prepareStatement("DELETE FROM `customer` WHERE `Cust_Id` = ? AND `Cust_Name` = ? AND `Phone` = ? AND `Email` = ? AND `Branch_Name`=? ");
                    pstmt.setInt(1, custPk);
                    pstmt.setString(2, custName);
                    pstmt.setString(3, custPhone);
                    pstmt.setString(4, custEmail);
                    pstmt.setString(5, branch);
                    int rowsAffected = pstmt.executeUpdate();
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Customer Deleted Successfully");
                        setVisible(true);
                        loadCustomerData();
                        clearForm();
                    } else {
                        JOptionPane.showMessageDialog(null, "Customer Not Found");
                    }

                } catch (Exception e) {
                    JOptionPane.showInternalMessageDialog(null, e);

                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

