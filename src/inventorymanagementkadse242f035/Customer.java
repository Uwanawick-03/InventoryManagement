    private boolean validateFields(String formType) {
        if (formType.equals("edit")
                && !txtCustName.getText().equals("")
                && !txtCustPhone.getText().equals("")
                && !txtCustEmail.getText().equals("")) {
            return false;
        } else if (formType.equals("new")
                && !txtCustName.getText().equals("")
                && !txtCustPhone.getText().equals("")
                && !txtCustEmail.getText().equals("")) {
            return false;

        } else {
            return true;
        }
    }

